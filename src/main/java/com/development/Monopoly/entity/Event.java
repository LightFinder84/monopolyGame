package com.development.Monopoly.entity;

public class Event {
    private String gamePlayMessage;
    private String rollDiceMessage;
    private String playerInTurnMessage;

    // getter and setter
    public String getGamePlayMessage() {
        return gamePlayMessage;
    }
    public void setGamePlayMessage(String gamePlayMessage) {
        this.gamePlayMessage = gamePlayMessage;
    }

    public String getPlayerInTurnMessage() {
        return playerInTurnMessage;
    }
    public void setPlayerInTurnMessage(String playerInTurnMessage) {
        this.playerInTurnMessage = playerInTurnMessage;
    }

    public String getRollDiceMessage() {
        return rollDiceMessage;
    }
    public void setRollDiceMessage(String rollDiceMessage) {
        this.rollDiceMessage = rollDiceMessage;
    }

   

    // constructor 
    
}
